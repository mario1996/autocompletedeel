// the timeout is not need, it's just to give us more time to see the loading while doing the search
export const getSearchData = (key: string): Promise<User[]> => {
  return new Promise((resolve, reject) => {
    setTimeout(() => {
      try {
        const filteredData = data.filter((user) => {
          return user.name.toLowerCase().includes(key.toLowerCase());
        });
        resolve(filteredData);
      } catch (error) {
        reject(new Error("Word not found"));
      }
    }, 1000);
  });
};
